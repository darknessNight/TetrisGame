#include "SdlException.h"

namespace SdlLibrary {

	Exception::Exception(NonSTL::string mess, int c)
	{
		message = mess;
		code = c;
	}
	//-----------------------------------------------------------------------------------------------------------------------------------------
	Exception::~Exception()
	{
	}
	//-----------------------------------------------------------------------------------------------------------------------------------------
	const NonSTL::string Exception::Message()
	{
		return this->message;
	}
	//-----------------------------------------------------------------------------------------------------------------------------------------
	const int Exception::Code()
	{
		return code;
	}
}